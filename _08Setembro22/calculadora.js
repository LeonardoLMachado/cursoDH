function somar(num1, num2) {
  return num1 + num2;
}
function subtrair(num1, num2) {
  return num1 - num2;
}
function multiplicar(num1, num2) {
  return num1 * num2;
}
function dividir(num1, num2) {
  return num1 / num2;
}

function calculadora(num1, num2, operacao) {
  return operacao(num1, num2);
}

